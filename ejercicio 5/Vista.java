public class Vista
{
    public void mostrarProceso(String mensaje)
    {
        System.out.println(mensaje);
    }

    public void iniciar()
    {
        System.out.println("Iniciando sistema operativo...");
        System.out.println("Sistema operativo iniciado.");
        System.out.println("Ejecutando procesos...");
        System.out.println("\n>>>>>");
    }

    public void apagar()
    {
        System.out.println("Apagando sistema operativo...");
        System.out.println("Sistema operativo apagado.");
    }

    public void enter()
    {
        System.out.println("");
    }
}

